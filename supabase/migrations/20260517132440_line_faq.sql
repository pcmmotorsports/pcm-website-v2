-- LINE 常見問題自動回覆表
-- 客戶訊息命中關鍵字時，webhook 自動以此表的答覆回覆

create table line_faq (
  id          uuid        primary key default gen_random_uuid(),
  title       text        not null,            -- 後台辨識用標題
  keywords    text[]      not null,            -- 觸發關鍵字
  answer      text        not null,            -- 自動回覆內容
  enabled     boolean     not null default true,
  sort_order  int         not null default 0, -- 多筆命中時取 sort_order 小的
  created_at  timestamptz not null default now(),
  updated_at  timestamptz not null default now()
);

-- 只有 service_role 能讀寫
alter table line_faq enable row level security;

